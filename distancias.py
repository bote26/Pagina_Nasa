class Distancias:
    def __init__(self, distancia):
        self.distancia = distancia

    def starTrek(self):
        return self.distancia / 585.58

    def starWars(self):
        return self.distancia / 1840491  
    
    def SpaceOdyssey(self):
        return self.distancia / 0.00031 
    
    def halo(self):
        return self.distancia / 102147.24
    
